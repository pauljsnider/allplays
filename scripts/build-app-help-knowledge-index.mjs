import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = path.join(repoRoot, 'apps/app/src/lib/helpKnowledgeIndex.ts');
const manifestPath = path.join(repoRoot, 'workflow-manifest.json');
const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
const manifestByFile = new Map(manifest.map((item) => [item.file, item]));
const helpFiles = fs.readdirSync(repoRoot)
  .filter((file) => /^(help.*|workflow-.*)\.html$/i.test(file))
  .sort();

const docs = helpFiles.map((file) => {
  const html = fs.readFileSync(path.join(repoRoot, file), 'utf8');
  const manifestItem = manifestByFile.get(file);
  const textFromHtml = rewriteAppHelpReferences(extractHtmlText(html), file);
  const title = manifestItem?.title || extractTitle(html, file);
  const summary = compactText(manifestItem?.summary || extractDescription(html) || firstUsefulSentence(textFromHtml));
  const text = compactStructuredText([
    title,
    summary,
    rewriteAppHelpReferences(manifestItem?.searchText || '', file),
    textFromHtml
  ].filter(Boolean).join('\n')).slice(0, 12000);

  return {
    id: manifestItem?.id || file.replace(/\.html$/i, ''),
    title,
    file,
    roles: normalizeRoles(manifestItem?.roles || inferRoles(textFromHtml)),
    summary,
    text
  };
});

const source = `// Generated by scripts/build-app-help-knowledge-index.mjs. Do not edit by hand.\n` +
  `export type HelpKnowledgeIndexDoc = {\n` +
  `  id: string;\n` +
  `  title: string;\n` +
  `  file: string;\n` +
  `  roles: string[];\n` +
  `  summary: string;\n` +
  `  text: string;\n` +
  `};\n\n` +
  `export const helpKnowledgeIndex: HelpKnowledgeIndexDoc[] = ${JSON.stringify(docs, null, 2)};\n`;

fs.writeFileSync(outputPath, source);
console.log(`Wrote ${docs.length} help docs to ${path.relative(repoRoot, outputPath)}`);

function extractHtmlText(html) {
  return compactStructuredText(html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<nav[\s\S]*?<\/nav>/gi, ' ')
    .replace(/<footer[\s\S]*?<\/footer>/gi, ' ')
    .replace(/<svg[\s\S]*?<\/svg>/gi, ' ')
    .replace(/<h[1-3][^>]*>([\s\S]*?)<\/h[1-3]>/gi, '\n$1\n')
    .replace(/<li[^>]*>([\s\S]*?)<\/li>/gi, '\n- $1\n')
    .replace(/<p[^>]*>([\s\S]*?)<\/p>/gi, '\n$1\n')
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&mdash;/g, '-')
    .replace(/&rarr;|-&gt;/g, '->')
    .replace(/&#39;/g, "'")
    .replace(/&quot;/g, '"'));
}

function extractTitle(html, file) {
  const heading = html.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i)?.[1]
    || html.match(/<title[^>]*>([\s\S]*?)<\/title>/i)?.[1]
    || file.replace(/[-_]/g, ' ').replace(/\.html$/i, '');
  return compactText(heading.replace(/<[^>]+>/g, ' '));
}

function extractDescription(html) {
  const description = html.match(/<meta[^>]+name=["']description["'][^>]+content=["']([^"']+)["']/i)?.[1]
    || html.match(/<p[^>]*>([\s\S]*?)<\/p>/i)?.[1]
    || '';
  return compactText(description.replace(/<[^>]+>/g, ' '));
}

function firstUsefulSentence(text) {
  return compactText(text)
    .split(/(?<=[.!?])\s+|\n+/)
    .map((sentence) => compactText(sentence))
    .find((sentence) => sentence.length > 40) || '';
}

function inferRoles(text) {
  const lower = text.toLowerCase();
  const roles = [];
  ['parent', 'coach', 'admin', 'member'].forEach((role) => {
    if (lower.includes(role)) roles.push(role);
  });
  return roles.length ? roles : ['all'];
}

function normalizeRoles(roles) {
  const normalized = roles
    .map((role) => compactText(role).toLowerCase())
    .map((role) => role === 'administrator' ? 'admin' : role)
    .map((role) => role === 'parents' ? 'parent' : role)
    .map((role) => role === 'coaches' ? 'coach' : role)
    .filter(Boolean);
  return [...new Set(normalized)];
}

function compactText(value) {
  return String(value || '').replace(/\s+/g, ' ').trim();
}

function compactStructuredText(value) {
  return String(value || '')
    .split(/\n+/)
    .map((line) => compactText(line))
    .filter(Boolean)
    .join('\n');
}

function rewriteAppHelpReferences(value, file) {
  if (file === 'help-page-reference.html') return String(value || '');

  return String(value || '')
    .replace(/(?<![-\w])login\.html\b/g, '#/auth')
    .replace(/(?<![-\w])reset-password\.html\b/g, '#/reset-password')
    .replace(/(?<![-\w])accept-invite\.html\b/g, '#/accept-invite')
    .replace(/(?<![-\w])verify-pending\.html\b/g, '#/verify-pending')
    .replace(/(?<![-\w])profile\.html\b/g, '#/profile')
    .replace(/(?<![-\w])admin\.html\b/g, 'admin tools');
}
